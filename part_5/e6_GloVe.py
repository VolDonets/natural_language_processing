# GloVe ~ Global Vectors for Word Representation
# GloVe produces the global vectors of word co-occurrences (co-occurrences across the entire corpus)
# that is familiar to weight matrix produced by backpropagation in Word2vec this one use SVD approach.

# GloVe Vs Word2vec
# Glove cons:
# + Faster training
# + Better RAM / CPU efficiency (can handle large documents)
# + More efficient use of data (helps with smaller corpora)
# + More accurate for the same amount of training
